package org.broadbandforum.tr098.internetgatewaydevice.laninterfaces;

import javax.xml.bind.annotation.XmlElement;
import org.broadbandforum.annotation.CWMPObject;
import org.broadbandforum.annotation.CWMPParameter;
import org.broadbandforum.tr098.datatypes.Alias;

	/**
	 * This object models an 802.11 LAN connection on a CPE device.

The object definition is identical to that for {{object|InternetGatewayDevice.LANDevice.{i}.WLANConfiguration.{i}}}.
	 *
	 * @since 1.4
	 */
@CWMPObject(name = "InternetGatewayDevice.LANInterfaces.WLANConfiguration.{i}.")
public class WLANConfiguration {

	/**
	 * {{datatype|expand}}
	 *
	 * @since 1.9
	 */
	@XmlElement(name = "Alias")
	@CWMPParameter(access = "readWrite", activeNotify = "canDeny")
	public Alias alias;

	public WLANConfiguration() {
	}

	//<editor-fold defaultstate="collapsed" desc="Getter and Setter">
	/**
	 * Get the {{datatype|expand}}
	 *
	 * @since 1.9
	 * @return the value
	 */
	public Alias getAlias() {
		return alias;
	}

	/**
	 * Set the {{datatype|expand}}
	 *
	 * @since 1.9
	 * @param alias the input value
	 */
	public void  setAlias(Alias alias) {
		this.alias = alias;
	}

	/**
	 * Set the {{datatype|expand}}
	 *
	 * @since 1.9
	 * @param alias the input value
	 * @return this instance
	 */
	public WLANConfiguration withAlias(Alias alias) {
		this.alias = alias;
		return this;
	}

	//</editor-fold>

}